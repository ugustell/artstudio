function AccordionItem({ question, answer }) {
  const [open, setOpen] = useState(false);
  return (
    <div className={`border-b border-white/10 transition-colors ${open ? 'border-primary/20' : ''}`}>
      <button
        onClick={() => setOpen(!open)}
        className="w-full text-left flex items-start justify-between gap-4 py-5 group">
        <span className={`font-semibold transition-colors duration-200 ${open ? 'text-primary' : 'text-on-surface group-hover:text-secondary'}`}>
          {question}
        </span>
        <span className={`text-2xl shrink-0 transition-transform duration-300 text-on-surface/40 ${open ? 'rotate-45 text-primary' : ''}`}>
          +
        </span>
      </button>
      <div className={`overflow-hidden transition-all duration-500 ${open ? 'max-h-96' : 'max-h-0'}`}>
        <p className="text-on-surface/60 leading-relaxed pb-5 text-sm">{answer}</p>
      </div>
    </div>
  );
}

